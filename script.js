const header = document.querySelector(".site-header");
const languageToggle = document.querySelector(".language-toggle");
const languageLabel = document.querySelector("[data-lang-label]");
let currentLanguage = localStorage.getItem("thermo-lio-language") || "zh";
let lastKnownY = 0;

function applyLanguage(language) {
  currentLanguage = language === "en" ? "en" : "zh";
  document.documentElement.lang = currentLanguage === "en" ? "en" : "zh-CN";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    const value = translations[currentLanguage][key];
    if (value) node.textContent = value;
  });
  if (languageLabel) languageLabel.textContent = currentLanguage === "en" ? "中文" : "EN";
  if (languageToggle) {
    languageToggle.setAttribute("aria-label", currentLanguage === "en" ? "Switch to Chinese" : "Switch to English");
  }
  localStorage.setItem("thermo-lio-language", currentLanguage);
}

function updateHeaderShadow() {
  const y = window.scrollY || document.documentElement.scrollTop;
  if (Math.abs(y - lastKnownY) < 8) return;
  lastKnownY = y;
  header.style.boxShadow = y > 20 ? "0 10px 30px rgba(15, 48, 105, 0.12)" : "none";
}

languageToggle?.addEventListener("click", () => {
  applyLanguage(currentLanguage === "en" ? "zh" : "en");
});

window.addEventListener("scroll", updateHeaderShadow, { passive: true });
applyLanguage(currentLanguage);
updateHeaderShadow();
